# –°–∫—Ä–∏–ø—Ç –¥–ª—è –∞–Ω–∞–ª–∏–∑–∞ —Å—Ç—Ä—É–∫—Ç—É—Ä—ã –∏ —Å–æ–¥–µ—Ä–∂–∏–º–æ–≥–æ —Ç—Ä–µ–Ω–∏—Ä–æ–≤–æ—á–Ω–æ–≥–æ –¥–∞—Ç–∞—Å–µ—Ç–∞
# –æ—Å–æ–±–µ–Ω–Ω–æ –ø–æ–ª–µ–∑–µ–Ω –ø—Ä–∏ —Ä–∞–±–æ—Ç–µ —Å –∏—Å–∫—É—Å—Å—Ç–≤–µ–Ω–Ω—ã–º —è–∑—ã–∫–æ–º Lincos, —Å–æ–¥–µ—Ä–∂–∞—â–∏–º –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–∏–µ —Å–∏–º–≤–æ–ª—ã

import pandas as pd  # –¥–ª—è –∑–∞–≥—Ä—É–∑–∫–∏ –∏ –æ–±—Ä–∞–±–æ—Ç–∫–∏ —Ç–∞–±–ª–∏—á–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
import json          # –∏–º–ø–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω, –Ω–æ –≤ —Ç–µ–∫—É—â–µ–π –≤–µ—Ä—Å–∏–∏ –Ω–µ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è (–æ—Å—Ç–∞–≤–ª–µ–Ω –Ω–∞ —Å–ª—É—á–∞–π –±—É–¥—É—â–µ–≥–æ —Ä–∞—Å—à–∏—Ä–µ–Ω–∏—è)


def analyze_dataset():
    """–ê–Ω–∞–ª–∏–∑–∏—Ä—É–µ—Ç —Ç—Ä–µ–Ω–∏—Ä–æ–≤–æ—á–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ: –ø–æ–∫–∞–∑—ã–≤–∞–µ—Ç –ø—Ä–∏–º–µ—Ä—ã –∏ –ø—Ä–æ–≤–µ—Ä—è–µ—Ç –Ω–∞–ª–∏—á–∏–µ –∫–ª—é—á–µ–≤—ã—Ö —Å–∏–º–≤–æ–ª–æ–≤."""

    # –ó–∞–≥—Ä—É–∂–∞–µ–º —Ç—Ä–µ–Ω–∏—Ä–æ–≤–æ—á–Ω—ã–π –¥–∞—Ç–∞—Å–µ—Ç –∏–∑ CSV-—Ñ–∞–π–ª–∞
    # –û–∂–∏–¥–∞–µ—Ç—Å—è, —á—Ç–æ —Ñ–∞–π–ª —Å–æ–¥–µ—Ä–∂–∏—Ç –¥–≤–µ –∫–æ–ª–æ–Ω–∫–∏: 'russian' –∏ 'lincos'
    train_df = pd.read_csv('lincos_dataset/train/train.csv')

    # –í—ã–≤–æ–¥–∏–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ –∞–Ω–∞–ª–∏–∑–∞
    print("üìä –ê–ù–ê–õ–ò–ó –¢–†–ï–ù–ò–†–û–í–û–ß–ù–´–• –î–ê–ù–ù–´–•")
    print("=" * 50)

    # –í—ã–≤–æ–¥–∏–º –ø–µ—Ä–≤—ã–µ 10 –ø—Ä–∏–º–µ—Ä–æ–≤ –ø–µ—Ä–µ–≤–æ–¥–∞ –¥–ª—è –≤–∏–∑—É–∞–ª—å–Ω–æ–π –ø—Ä–æ–≤–µ—Ä–∫–∏ –∫–∞—á–µ—Å—Ç–≤–∞ –¥–∞–Ω–Ω—ã—Ö
    print("–ü–µ—Ä–≤—ã–µ 10 –ø—Ä–∏–º–µ—Ä–æ–≤:")
    for i, (rus, lin) in enumerate(zip(train_df['russian'], train_df['lincos'])):
        if i >= 10:  # –æ–≥—Ä–∞–Ω–∏—á–∏–≤–∞–µ–º –≤—ã–≤–æ–¥ –ø–µ—Ä–≤—ã–º–∏ 10 —Å—Ç—Ä–æ–∫–∞–º–∏
            break
        print(f"{i + 1}. üá∑üá∫ {rus}")   # –∏—Å—Ö–æ–¥–Ω–æ–µ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–µ –Ω–∞ —Ä—É—Å—Å–∫–æ–º
        print(f"   ü™ê {lin}")          # —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–∏–π –ø–µ—Ä–µ–≤–æ–¥ –Ω–∞ Lincos
        print()                       # –ø—É—Å—Ç–∞—è —Å—Ç—Ä–æ–∫–∞ –¥–ª—è —á–∏—Ç–∞–µ–º–æ—Å—Ç–∏

    # –°–æ–±–∏—Ä–∞–µ–º —É–Ω–∏–∫–∞–ª—å–Ω—ã–µ —Å–∏–º–≤–æ–ª—ã, –≤—Å—Ç—Ä–µ—á–∞—é—â–∏–µ—Å—è –≤–æ –≤—Å–µ—Ö Lincos-–ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏—è—Ö
    all_lincos_chars = set()
    for text in train_df['lincos']:
        all_lincos_chars.update(text)  # –¥–æ–±–∞–≤–ª—è–µ–º –∫–∞–∂–¥—ã–π —Å–∏–º–≤–æ–ª —Å—Ç—Ä–æ–∫–∏ –≤ –º–Ω–æ–∂–µ—Å—Ç–≤–æ

    # –í—ã–≤–æ–¥–∏–º –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫ –≤—Å–µ—Ö —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö —Å–∏–º–≤–æ–ª–æ–≤ –≤ Lincos
    print("–°–∏–º–≤–æ–ª—ã –≤ –ª–∏–Ω–∫–æ—Å–µ:", sorted(all_lincos_chars))

    # –û–ø—Ä–µ–¥–µ–ª—è–µ–º —Å–ø–∏—Å–æ–∫ –æ–∂–∏–¥–∞–µ–º—ã—Ö –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–∏—Ö –∏ –ª–æ–≥–∏—á–µ—Å–∫–∏—Ö —Å–∏–º–≤–æ–ª–æ–≤,
    # —Ö–∞—Ä–∞–∫—Ç–µ—Ä–Ω—ã—Ö –¥–ª—è —è–∑—ã–∫–∞ Lincos (–∫–∞–∫ –≤ –æ—Ä–∏–≥–∏–Ω–∞–ª—å–Ω—ã—Ö —Ä–∞–±–æ—Ç–∞—Ö Freudenthal)
    math_symbols = ['+', '-', '√ó', '√∑', '=', '‚àà', '‚àÄ', '‚àÉ', '‚áí', '‚àß', '‚à®', '¬¨']

    # –ù–∞—Ö–æ–¥–∏–º, –∫–∞–∫–∏–µ –∏–∑ –æ–∂–∏–¥–∞–µ–º—ã—Ö —Å–∏–º–≤–æ–ª–æ–≤ –æ—Ç—Å—É—Ç—Å—Ç–≤—É—é—Ç –≤ –¥–∞—Ç–∞—Å–µ—Ç–µ
    missing_symbols = [sym for sym in math_symbols if sym not in all_lincos_chars]

    # –°–æ–æ–±—â–∞–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é –æ –Ω–∞–ª–∏—á–∏–∏ –∏–ª–∏ –æ—Ç—Å—É—Ç—Å—Ç–≤–∏–∏ –∫–ª—é—á–µ–≤—ã—Ö —Å–∏–º–≤–æ–ª–æ–≤
    if missing_symbols:
        print(f"‚ùå –û—Ç—Å—É—Ç—Å—Ç–≤—É—é—Ç —Å–∏–º–≤–æ–ª—ã: {missing_symbols}")
    else:
        print("‚úÖ –í—Å–µ –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–∏–µ —Å–∏–º–≤–æ–ª—ã –ø—Ä–∏—Å—É—Ç—Å—Ç–≤—É—é—Ç")


# –¢–æ—á–∫–∞ –≤—Ö–æ–¥–∞: –∑–∞–ø—É—Å–∫–∞–µ–º –∞–Ω–∞–ª–∏–∑ –ø—Ä–∏ –ø—Ä—è–º–æ–º –≤—ã–∑–æ–≤–µ —Å–∫—Ä–∏–ø—Ç–∞
if __name__ == "__main__":
    analyze_dataset()